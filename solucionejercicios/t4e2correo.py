# Pedir al usuario que introduzca una dirección de correo electrónico
correo = input("Introduce tu dirección de correo electrónico: ")

# Mostrar la longitud de la dirección de correo
longitud = len(correo)
print("La longitud de la dirección de correo es:", longitud)

# Mostrar la dirección de correo en mayúsculas
correo_mayusculas = correo.upper()
print("Correo en mayúsculas:", correo_mayusculas)

# Mostrar la dirección de correo en minúsculas
correo_minusculas = correo.lower()
print("Correo en minúsculas:", correo_minusculas)

